package com.zhou;

public class Field {
    private String fieldType;

    private String fieldName;

    private String fieldRemarks;

    private String fieldNameUpperLetter;

    public String getFieldType() {
        return fieldType;
    }

    public void setFieldType(String fieldType) {
        this.fieldType = fieldType;
    }

    public String getFieldName() {
        return fieldName;
    }

    public void setFieldName(String fieldName) {
        this.fieldName = fieldName;
    }

    public String getFieldRemarks() {
        return fieldRemarks;
    }

    public void setFieldRemarks(String fieldRemarks) {
        this.fieldRemarks = fieldRemarks;
    }

    public String getFieldNameUpperLetter() {
        return fieldNameUpperLetter;
    }

    public void setFieldNameUpperLetter(String fieldNameUpperLetter) {
        this.fieldNameUpperLetter = fieldNameUpperLetter;
    }
}
